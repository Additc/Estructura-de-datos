# Programa que imprime todos los numeros pares

numero_final=int(input("ingrese un numero: "))
contador = 2
while contador <= numero_final:
        print(contador, end=" ")  # La funcion end imprime en horizontal.
        contador += 2
print()
print("fin de cuenta")