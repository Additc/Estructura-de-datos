# programa que imprimer un ejemplo del ciclo while
numero=int(input("ingrese un numero: "))
contador = 1
while contador<= numero:
    print( contador , end =" ") # La funcion end imprime en horizontal.
    contador += 1
print()
print("fin de cuenta")